function Sidebar() {
  const user = useAuthStore((state) => state.user);
  const userInfo = user !== null ? user : "Usuario no identificado";
  // console.log(userInfo)
  // useMemo(() => {
  //   console.log("Usuario en store de zustand: ", user);
  // }, [user]);
  const [logo, setLogo] = useState(null);
  const handleLogout = () => {
    console.log("Cerrando sesión...");
    Cookies.remove("access");
    Cookies.remove("refresh");
    Cookies.remove("establishmentId");
    window.location.reload();
  };

  useEffect(() => {
    const fetchLogo = async () => {
      try {
        const response = await axios.get(
          `http://localhost:8000/image/get-logo/${Cookies.get(
            "establishmentId"
          )}`
        );
        setLogo(response.data.imagen_base64);
      } catch (err) {
        setLogo(
          "https://coffective.com/wp-content/uploads/2018/06/default-featured-image.png.jpg"
        );
        console.log(err);
      }
    };
    fetchLogo();
  }, []);

  const url = useLocation().pathname;

  return (
    <aside className="bg-slate-50 w-full grid py-2 2xl:py-6 grid-rows-[auto_1fr_auto] justify-center gap-4 2xl:gap-10 border-r-2 border-slate-200">
      <button className="md:hidden">
        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          strokeWidth={1.5}
          stroke="currentColor"
          className="size-6"
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25H12"
          />
        </svg>
      </button>
      {/* <h1 className="hidden lg:block font-amaranth font-bold text-5xl text-center">
        NiceLook.
      </h1> */}
      <div className="flex 2xl:gap-5 gap-3 flex-col items-center">
        <LogoNiceLook className="text-4xl" />
        <img src={logo} className="size-16 2xl:size-32 rounded-full"></img>
        <p>
          {userInfo.first_name} {userInfo.last_name}
        </p>
        <ButtonCustom secondary name="Configuración" />
      </div>
      <nav className="flex flex-col gap-2 2xl:gap-6">
        {url.includes("admin")
          ? adminNavLinks.map((link) => (
              <LinkSidebar
                key={link.name}
                name={link.name}
                path={link.path}
                icon={link.icon}
              />
            ))
          : url.includes("recepcionist")
          ? recepcionistNavLinks.map((link) => (
              <LinkSidebar
                key={link.name}
                name={link.name}
                path={link.path}
                icon={link.icon}
              />
            ))
          : employeeNavLinks.map((link) => (
              <LinkSidebar
                key={link.name}
                name={link.name}
                path={link.path}
                icon={link.icon}
              />
            ))}
      </nav>
      <div className="flex items-center justify-center">
        <Button onClick={handleLogout} color="danger" variant="light">
          Cerrar Sesión
        </Button>
      </div>
    </aside>
  );
}

export default Sidebar;
